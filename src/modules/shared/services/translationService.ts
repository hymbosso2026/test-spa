export const getTranslation = (language: string, key: string): string => {
  const selectedLanguage = T[language] ? language : DEFAULT_LANGUAGE;
  return T[selectedLanguage]?.[key] || T[DEFAULT_LANGUAGE]?.[key] || key;
};

